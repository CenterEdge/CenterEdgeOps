#!/usr/bin/env bash
set -euo pipefail

_envrootdirs=(
    './terraform'
)


for d in "${_envrootdirs[@]}"; do
  echo "************************************************************************************************"
  echo "------------------------------------------------------------------------------------------------"
  echo "************************************************************************************************"
  echo "                          Running Terraform Init for \"${d}\"                         "
  echo ""
  (cd "${d}"; terraform init)
done

echo "************************************************************************************************"
echo "------------------------------------------------------------------------------------------------"
echo "************************************************************************************************"
